def show_vida(vida, vidas):
    """muestra la vida como una barra con palitos

    Args:
        vida ([int]): [numero de vidas con las que inicio el jugador]
        vidas ([type]): [numero de vidas que tiene el jugador]

    Returns:
        [str]: [un string de palitos para representar la vida]
    """
    # Palitos por vida
    total = 20
    valor = total // vida

    if vidas < vida:
        espacios = int(valor * (vida - vidas))
    else:
        espacios = 0

    palitos = int(total - espacios)
    palitos = '|' * palitos
    espacios = ' ' * espacios
    life_bar = palitos + espacios
    return life_bar

